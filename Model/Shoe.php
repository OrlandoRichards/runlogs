      $this->id = $id;
      if (!$this->exists()) {
         throw new NotFoundException(__('Invalid shoe'));
      }
      $distance_km = $this -> query ("SELECT SUM(km) AS 'distance_km' FROM runs where shoe_id=$id");

      $this->set(array('distance_km'=>$distance_km[0][0]['distance_km']));
      $this->save();


